interface EmojiPickerProps {
  value: string;
  onChange: (emoji: string) => void;
}

export function EmojiPicker({ value, onChange }: EmojiPickerProps) {
  const [search, setSearch] = useState("");
  const [activeCategory, setActiveCategory] = useState(0);
  const gridRef = useRef<HTMLDivElement>(null);

  const searchResults = useMemo(() => {
    if (!search.trim()) return null;
    const all = CATEGORIES.flatMap((c) => c.emojis);
    return all.filter((e) => e.includes(search.trim()));
  }, [search]);

  const handleCategoryClick = (idx: number) => {
    setActiveCategory(idx);
    setSearch("");
  };

  const displayEmojis = searchResults ?? CATEGORIES[activeCategory].emojis;

  return (
    <div className="space-y-2">
      <div className="relative">
        <svg
          className="absolute left-2.5 top-1/2 -translate-y-1/2 w-3.5 h-3.5 text-muted-foreground"
          fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}
        >
          <path strokeLinecap="round" strokeLinejoin="round" d="M21 21l-4.35-4.35m0 0A7.5 7.5 0 104.5 4.5a7.5 7.5 0 0012.15 12.15z" />
        </svg>
        <Input
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          placeholder="Cari emoji..."
          className="pl-8 h-8 text-sm"
        />
      </div>

      {!search && (
        <div className="flex gap-0.5 overflow-x-auto pb-1 scrollbar-none">
          {CATEGORIES.map((cat, idx) => (
            <button
              key={cat.label}
              type="button"
              onClick={() => handleCategoryClick(idx)}
              title={cat.label}
              className={cn(
                "shrink-0 w-8 h-8 rounded-md text-base flex items-center justify-center transition-colors",
                activeCategory === idx
                  ? "bg-primary/20 ring-1 ring-primary"
                  : "hover:bg-muted text-muted-foreground"
              )}
            >
              {cat.icon}
            </button>
          ))}
        </div>
      )}

      {!search && (
        <p className="text-[10px] font-semibold uppercase tracking-wider text-muted-foreground">
          {CATEGORIES[activeCategory].label}
        </p>
      )}

      <div
        ref={gridRef}
        className="grid grid-cols-9 gap-0.5 max-h-[200px] overflow-y-auto pr-1"
        style={{ scrollbarWidth: "thin" }}
      >
        {displayEmojis.map((em, i) => (
          <button
            key={`${em}-${i}`}
            type="button"
            onClick={() => onChange(em)}
            className={cn(
              "w-8 h-8 rounded-md text-lg flex items-center justify-center transition-colors leading-none",
              value === em
                ? "bg-primary/20 ring-2 ring-primary"
                : "hover:bg-muted"
            )}
          >
            {em}
          </button>
        ))}
        {displayEmojis.length === 0 && (
          <div className="col-span-9 text-center py-4 text-sm text-muted-foreground">
            Tidak ditemukan
          </div>
        )}
      </div>
    </div>
  );
}
